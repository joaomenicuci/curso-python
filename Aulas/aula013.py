#   Estrutura de Repetição
    # Tipo de Condição:     for:

#   Exercício 1

for c in range(1,11):
    print(c)
print('Acabou')

#   Exercício 2

for c in range(10, 0, -1):
    print(c)
print('Acabou')

#   Exercício 3

for c in range(1,11, 2):
    print(c)
print('Acabou')

#   Exercício 4

n = int(input('Digite um número: '))
for c in range(0, n+1):
    print(c)
print('Fim')
